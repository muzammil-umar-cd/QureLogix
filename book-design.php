                        <img src="services-book-design/images/book-cover-design-services.jpg" width="486" height="268.2" alt="cover and interior design services" />
                    </picture>
                </div>

                <div class="vspace-050em"></div>
            </div>

            <h4><span class="bolded">3. Review a Complete Proof</span></h4>
            <p>After the designer reviews your feedback and discusses it with you, they will make any required alterations to the design and finish the
                complete design. You will then receive a complete proof of the book. You will be able to either make changes
                (<a href="javascript:;">Additional Design Changes & Proofs Rates</a>) or approve for production.</p>

            <div class="vspace-1em auto-width"></div>

