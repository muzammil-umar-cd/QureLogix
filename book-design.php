                $('.design-pairing').hide();
                $('.design-pieces, .design-pricing').css({
                    opacity: 0,
                    display: 'inline-block'
                }).animate({
                    opacity: 1
                }, 250);
            });
        });


        $('input[name=binding]').change(function() {
            if ($('.design-toggle').css('opacity') != 1) {
                $('.design-toggle').removeClass('fadedout').css('pointer-events', 'auto').animate({
                    opacity: 1
                }, 300);
            }
        });

        $('input[name=design-toggle]').change(function() {
            if ($('input[name=design-toggle]:checked').val() == 'packages') {
                if ($('.design-inputs').is(':hidden')) {
                    $('.design-levels').css({
                        opacity: 0,
                        display: 'inline-block'
                    }).animate({
                        opacity: 1
                    }, 250);
                } else {
                    $('.design-inputs').animate({
                        opacity: 0
                    }, 250).promise().done(function() {
                        $('.design-inputs').hide();
                        $('.design-levels').css({
                            opacity: 0,
                            display: 'inline-block'
                        }).animate({
                            opacity: 1
                        }, 250);
                    });
                }
            } else {
                if ($('.design-levels').is(':hidden')) {
                    $('.design-inputs').css({
                        opacity: 0,
                        display: 'inline-block'
                    }).animate({
                        opacity: 1
                    }, 250);
                } else {
