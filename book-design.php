            <div class="vspace-1em auto-width"></div>

            <h4><span class="bolded">Why do I have to send in a word document and my images separately?</span></h4>
            <p>When designing a book, we will flow your words into Adobe InDesign. From there we will choose fonts, styles, margins, etc.
                If your images are in your manuscript, they won’t flow into our software and will be lost. Images placed in word documents
                are also condensed and lower quality. We will manually optimize images and then place them in the InDesign file after the
                text is formed. This is why we need place markers, such as "insert image x here", in the manuscript to indicate where your
                images should go.</p>
