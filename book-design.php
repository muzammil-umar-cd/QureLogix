            <p>You have already put so much work into writing your book, so let us help you get across the finish line. Our goal is to make the
                book design process simple, easy, and hands-free for you. Our designers review each book individually and will work to create a
                one-of-a-kind book format/design for you.</p>

            <div class="vspace-1em auto-width"></div>

            <h4><span class="bolded">Before Starting an Order:</span></h4>

            <p>Since designers do not read or edit your book, it is important you have your manuscript completely edited. You can do this
                yourself, have a friend or family member do it, or hire a professional editor. We do not have editors in-house but have a great
                list of <a href="javascript:;">Recommended Editors</a>.</p>

            <div class="vspace-1em auto-width"></div>

            <h4><span class="bolded">1. Start an Order</span></h4>
            <p>To get started you will start an order with a quote and half-down deposit by giving us a call. After submitting your deposit, you will either upload or mail your materials to us.</p>

            <div class="vspace-1em auto-width"></div>

            <h4><span class="bolded">2. Review a Preview of the Design</span></h4>
            <p>One of our expert book designers will review all of your materials including your preferences, book genre, etc. After this, they will create
                a preview of the book design concept. Usually a low-res of the cover or a few pages of the interior. You will have a day or two to review the
                preview and send feedback to your designer.</p>

            <div class="vspace-1em"></div>

            <div class="box-055 last right text-right">
                <div class="vspace-050em"></div>

                <div class="grid-090">
                    <picture>
