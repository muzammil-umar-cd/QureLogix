                        opacity: 0
                    }, 250).promise().done(function() {
                        $('.design-levels').hide();
                        $('.design-inputs').css({
                            opacity: 0,
                            display: 'inline-block'
                        }).animate({
                            opacity: 1
                        }, 250);
                    });
                }
            }
        });

    });
